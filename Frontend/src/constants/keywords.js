/**
 * Normaliza texto removiendo acentos y convirtiendo a minúsculas
 */
const normalizeText = (text) => {
  return text.toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();
};

/**
 * Detecta la categoría basándose en las palabras del input del usuario
 */
export const detectCategory = (input) => {
  if (!input || input.length < 3) return null;
  
  const normalizedInput = normalizeText(input);
  const scores = {};
  
  // Calcular puntuación para cada categoría
  Object.entries(CONTEXT_KEYWORDS).forEach(([category, keywords]) => {
    let score = 0;
    keywords.forEach(keyword => {
      const normalizedKeyword = normalizeText(keyword);
      if (normalizedInput.includes(normalizedKeyword)) {
        // Dar más peso a coincidencias exactas de palabras completas
        const regex = new RegExp(`\\b${normalizedKeyword.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'i');
        score += regex.test(normalizedInput) ? 3 : 1;
      }
    });
    if (score > 0) {
      scores[category] = score;
    }
  });
  
  // Retornar la categoría con mayor puntuación
  if (Object.keys(scores).length === 0) return null;
  
  const sortedCategories = Object.entries(scores)
    .sort((a, b) => b[1] - a[1]);
  
  return sortedCategories[0][0];
};

/**
 * Obtiene sugerencias contextuales inteligentes basadas en el input
 */
export const getContextualSuggestions = (input) => {
  if (!input || input.length < 3) {
    return {
      category: null,
      suggestions: GENERAL_SUGGESTIONS.slice(0, 8)
    };
  }
  
  const detectedCategory = detectCategory(input);
  const normalizedInput = normalizeText(input);
  
  if (detectedCategory) {
    // Obtener sugerencias de la categoría detectada
    const categorySuggestions = CONTEXTUAL_SUGGESTIONS[detectedCategory] || [];
    
    // Ordenar por relevancia: priorizar las que contienen palabras del input
    const scoredSuggestions = categorySuggestions.map(suggestion => {
      let score = 0;
      const normalizedSuggestion = normalizeText(suggestion);
      
      // Dividir el input en palabras individuales
      const inputWords = normalizedInput.split(/\s+/);
      
      inputWords.forEach(word => {
        if (word.length >= 3) {
          // Coincidencia exacta de palabra completa: máxima prioridad
          const exactMatch = new RegExp(`\\b${word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'i');
          if (exactMatch.test(normalizedSuggestion)) {
            score += 10;
          }
          // Coincidencia parcial al inicio de palabra
          else if (normalizedSuggestion.includes(word)) {
            score += 5;
          }
          // Caracteres en común
          else {
            const commonChars = [...word].filter(char => normalizedSuggestion.includes(char)).length;
            score += commonChars * 0.5;
          }
        }
      });
      
      return { suggestion, score };
    });
    
    // Ordenar por puntuación descendente y tomar las mejores
    const sortedSuggestions = scoredSuggestions
      .sort((a, b) => b.score - a.score)
      .map(item => item.suggestion);
    
    return {
      category: detectedCategory,
      suggestions: sortedSuggestions
    };
  }
  
  // Si no se detecta categoría, buscar coincidencias parciales en todas las sugerencias
  const allSuggestions = [];
  
  Object.values(CONTEXTUAL_SUGGESTIONS).forEach(suggestions => {
    suggestions.forEach(suggestion => {
      if (suggestion.toLowerCase().includes(normalizedInput) && 
          !allSuggestions.includes(suggestion)) {
        allSuggestions.push(suggestion);
      }
    });
  });
  
  // Agregar sugerencias generales si hay pocas coincidencias
  if (allSuggestions.length < 5) {
    GENERAL_SUGGESTIONS.forEach(suggestion => {
      if (!allSuggestions.includes(suggestion)) {
        allSuggestions.push(suggestion);
      }
    });
  }
  
  return {
    category: null,
    suggestions: allSuggestions.slice(0, 10)
  };
};

